    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int i = tabel.getSelectedRow();
        
        if(i>-1){
            nama_pemain.setText(model.getValueAt(i,0).toString());
            umur_pemain.setText(model.getValueAt(i,1).toString());
            asal_klub.setText(model.getValueAt(i,2).toString());
            posisi.setText(model.getValueAt(i,3).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

