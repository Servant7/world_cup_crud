private void tampilkan() {
        //baris 477 membuat method tampilkan
        int row = tabel.getRowCount();
        for(int a= 0; a<row;a++){
            model.removeRow(0);
        }
        try {
           Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/world_cup","root","");
           //baris 484 mencoba untuk terhubung ke database
           ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM data_pemain");
           //baris 486 menggunakan sintaks SELECT*FROM nama_table sql untuk memilih data
           while(rs.next()){
               String data []={rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
               model.addRow(data);
           }
        } catch (SQLException ex) {
            Logger.getLogger(menu_utama.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
